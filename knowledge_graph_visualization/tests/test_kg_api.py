import requests
import json
import pytest

BASE_URL = 'http://localhost:8000/api'

def test_get_graph_data():
    """测试获取图谱数据接口"""
    response = requests.get(f'{BASE_URL}/kg/data')
    assert response.status_code == 200, "接口请求失败"
    data = response.json()
    assert data['ret'] == 0, "接口返回错误"
    assert 'nodes' in data['data'] and 'links' in data['data'], "数据格式错误"

def test_add_entity():
    """测试新增实体接口"""
    test_entity = {
        "id": "test_entity_123",
        "name": "测试实体",
        "type": "测试类型"
    }
    print(111)

    response = requests.post(
        f'{BASE_URL}/kg/entity',
        headers={'Content-Type': 'application/json'},
        data=json.dumps(test_entity)
    )
    assert response.status_code == 200, "接口请求失败"
    result = response.json()
    assert result['ret'] == 0, f"新增实体失败: {result.get('msg')}"

if __name__ == '__main__':
    pytest.main(['-v', 'test_kg_api.py'])